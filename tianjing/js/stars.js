export function getStarInfo(name) {
  return MAJOR_STARS[name] || MINOR_STARS[name] || null
}

export function collectChartStars(astrolabe) {
  const seen = new Map()

  for (const palace of astrolabe.palaces) {
    const all = [
      ...(palace.majorStars || []),
      ...(palace.minorStars || []),
      ...(palace.adjectiveStars || [])
    ]
    for (const star of all) {
      if (!star.name || seen.has(star.name)) continue
      const info = getStarInfo(star.name)
      if (info) {
        seen.set(star.name, {
          name: star.name,
          palace: palace.name,
          ...info
        })
      }
    }
  }

  const order = [...Object.keys(MAJOR_STARS), ...Object.keys(MINOR_STARS)]
  return [...seen.values()].sort(
    (a, b) => order.indexOf(a.name) - order.indexOf(b.name)
  )
}
