export function plainPalaceReading(palaceName, majorStars, oppositeMajor = []) {
  const intro = PALACE_INTRO[palaceName] || `关于${palaceName}的运势。`
  const tips = PALACE_TIPS[palaceName] || ''

  if (majorStars.length) {
    const starTexts = majorStars.map((star) => {
      const specific = STAR_PALACE_PLAIN[star]?.[palaceName]
      if (specific) return specific
      const trait = STAR_PLAIN[star] || `${star}带来独特影响。`
      return `因为${star}在这里，${trait}`
    })
    return `${intro}\n\n${starTexts.join('\n\n')}${tips ? `\n\n💡 建议：${tips}` : ''}`
  }

  if (oppositeMajor.length) {
    return `${intro}\n\n这宫没有主星，会「借」对宫的${oppositeMajor.join('、')}能量——外在表现可能像那些特质，但内心感受仍以本宫主题为主。\n\n💡 建议：${tips || '多观察自己在该领域是「外强」还是「内耗」，再调整策略。'}`
  }

  return `${intro}\n\n这宫主星不明显，该领域运势较随环境和大限流年变化，需结合整体命盘看。\n\n💡 建议：${tips || '保持灵活，别过早下定论。'}`
}
